/**
 * 音声合成用にテキストを変換する
 * 難読漢字をひらがなに置き換えて正しい読みで発音されるようにする
 */
export function convertToSpeechText(text: string): string {
  let result = text;
  for (const [pattern, reading] of READING_DICT) {
    result = result.replace(pattern, reading);
  }
  return result;
}
